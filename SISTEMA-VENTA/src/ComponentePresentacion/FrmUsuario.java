    private void btnBuscarUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarUActionPerformed
        // TODO add your handling code here:
        if (txtIDu.getText().compareTo("") != 0) {
            int result = JOptionPane.showConfirmDialog(
                    this, "Deseas Buscar para Modificar \n"
                    + "los Datos del Usuario?",
                    "Mensaje..!!",
                    JOptionPane.YES_NO_OPTION);

            if (result == JOptionPane.YES_OPTION) {
                lblIngresopass.setVisible(true);
                txtnewPassU.setVisible(true);
                BuscarUsuario();
                btnEditarU.setEnabled(true);
                btnMostrar.setEnabled(false);
                btnCancelarU.setEnabled(true);
                txtIDu.setEditable(false);
                txtPassU.setEditable(false);
                txtnewPassU.setEditable(true);
                cboEmpleadoU.setEnabled(false);

            }
            if (result == JOptionPane.NO_OPTION) {
                BuscarUsuario();
                btnEliminarU.setEnabled(true);
                btnCancelarU.setEnabled(true);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese ID que desea Buscar");
        }
}//GEN-LAST:event_btnBuscarUActionPerformed


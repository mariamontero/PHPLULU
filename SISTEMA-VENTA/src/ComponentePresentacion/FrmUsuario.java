    private void btnAgregarUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarUActionPerformed
        // TODO add your handling code here:
        if (txtIDu.getText().compareTo("") != 0 && txtPassU.getText().compareTo("") != 0) {
            Usuario u = new Usuario() {
            };
            u.setId(txtIDu.getText());
            String pass = new String(txtPassU.getPassword());
            u.setPassword(pass);
            try {
                Empleado e = BDEmpleado.buscarEmpleadoApe((String) cboEmpleadoU.getSelectedItem());
                u.setEmpleado(e);
                BDUsuario.insertarUsuario(u);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
            }
            JOptionPane.showMessageDialog(null, "[ DATOS AGREGADOS ]");
        }
}//GEN-LAST:event_btnAgregarUActionPerformed


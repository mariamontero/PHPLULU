    public void BuscarUsuario() {
        Usuario u = null;
        try {
            u = BDUsuario.buscarUsuario(txtIDu.getText());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
        }
        if (u != null) {
            txtPassU.setText(u.getPassword());
            cboEmpleadoU.setSelectedItem(u.getEmpleado().getApellido());
            txtIDu.setEditable(false);
        } else {
            JOptionPane.showMessageDialog(null, "No exite");
        }
    }

